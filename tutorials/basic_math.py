# variables
x = 10
y = 10

# addition
add_result = x + y
print(f"x + y is {add_result} for x = {x} and y = {y}.\n")

# subtraction
sub_result = y - x
print(f"y - x is {sub_result} for x = {x} and y = {y}.\n")

# division
div_result = y / x
print(f"y/x is {div_result} for x = {x} and y = {y}.\n")

# multiplication
mult_result = x * y
print(f"x * y is {mult_result} for x = {x} and y = {y}.\n")
